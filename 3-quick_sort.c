#include "sort.h"

void swap_ints(int *a, int *b);
int lomuto_partition(int *array, size_t size, int left, int right);
void lomuto_sort(int *array, size_t size, int left, int right);
void quick_sort(int *array, size_t size);

/**
 * swap_ints - Transform two array elements by swapping values.
 * @a: Pointer to the first integer to be exchanged.
 * @b: Pointer to the second integer to be exchanged.
 */
void swap_ints(int *a, int *b)
{
	int tmp;

	tmp = *a;
	*a = *b;
	*b = tmp;
}

/**
 * lomuto_partition - Orchestrates a Lomuto pivot-based sort within a subset.
 *
 * Reorganizes integers in array[left, right] around the pivot (last element).
 *
 * @array: Array of integers.
 * @size: Size of the array.
 * @left: Starting index of the subset to order.
 * @right: Ending index of the subset to order.
 *
 * Return:Index marking the final partition.
 */
int lomuto_partition(int *array, size_t size, int left, int right)
{
	int *driver, up, down;

	driver = array + right;
	for (up = down = left; down < right; down++)
	{
		if (array[down] < *driver)
		{
			if (up < down)
			{
				swap_ints(array + down, array + up);
				print_array(array, size);
			}
			up++;
		}
	}

	if (array[up] > *driver)
	{
		swap_ints(array + up, driver);
		print_array(array, size);
	}

	return (up);
}

/**
 * lomuto_sort - Utilize the quicksort algorithm with Lomuto's
 * partition scheme to efficiently sort an array of integers.
 *
 * @array: Array of integers to be sorted.
 * @size: Size of the array.
 * @left: Starting index of the array partition to be ordered.
 * @right: Ending index of the array partition to be ordered.
 */
void lomuto_sort(int *array, size_t size, int left, int right)
{
	int belong;

	if (right - left > 0)
	{
		belong = lomuto_partition(array, size, left, right);
		lomuto_sort(array, size, left, belong - 1);
		lomuto_sort(array, size, belong + 1, right);
	}
}

/**
 * quick_sort - Efficiently sort an array of integers
 *                         in ascending order using quicksort.
 * @array: Array of integers.
 * @size: Size of the array.
 *
 * Description: Employs the Lomuto partition scheme, showcasing
 *              the array's transformation after every swap.
 */
void quick_sort(int *array, size_t size)
{
	if (array == NULL || size < 2)
		return;

	lomuto_sort(array, size, 0, size - 1);
}
